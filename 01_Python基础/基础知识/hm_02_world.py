"""
   数字类型包括： int float complex bool
   非数字类型： list列表[] tuple元祖()  dict字典{}  str字符串""/''
   所有非数字类型，都可以使用for循环
"""
# 数据类型
print("hello world")
print(type(12)) # int
print(type(12.2))  # float
print(type(True))  # bool
print(type(1+3j))  # complex
print(type(["zhangsan",12,12.3]))  # list
print(type(("zhangsan",12,12.3)))   # tuple
print(type({"name":"zhangsan","age":12,"high":12.3}))  # dict
print(type("12"))  # str
